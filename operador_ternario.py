# Usamos el operador ternario para asignar un valor dependiendo de una condición
edad = 20

# Si la edad es mayor o igual a 18, es mayor de edad, de lo contrario, es menor de edad
estado = "Mayor de edad" if edad >= 18 else "Menor de edad"

print(estado)  # Salida: Mayor de edad


numero = 5

# Determinar si el número es par o impar
resultado = "Par" if numero % 2 == 0 else "Impar"

print(resultado)  # Salida: Impar